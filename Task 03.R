 Иван има 4 ключа, но не знае кой е за неговата стая. Предполагаме, че клю
човете са разбъркани по произволен начин. Иван пробва последователно с всеки от тях,
 като помни кой ключ е пробвал. Каква е вероятността да отключи с последния (четвъртия)
 ключ?

sim.keys <-function()
{
  keys <- c(1:4)
  x <-sample(keys,4,replace=F)
  x[4]==1
}
sim.keys()
prob.keys <-function(Nrep)
{
  res <- replicate(Nrep,sim.keys())
  sum(res)/length(res)
}
prob.keys(100000)
